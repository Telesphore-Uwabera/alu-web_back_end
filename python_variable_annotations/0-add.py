#!/usr/bin/env python3
"""
Module 0-add
Contains a function to add two float numbers.
"""
def add(a: float, b: float) -> float:
    """
    Add two float numbers and return the result.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of the two numbers.
    """
    return a + b
